 WHERE NUMGUNS>=ALL(SELECT NUMGUNS FROM CLASSES)

SELECT NAME FROM SHIPS
	WHERE CLASS IN (SELECT CLASS FROM CLASSES WHERE BORE=16)

SELECT NAME FROM BATTLES
	WHERE NAME IN(SELECT BATTLE FROM OUTCOMES WHERE SHIP IN (SELECT NAME FROM SHIPS WHERE CLASS='Kongo'))

SELECT CLASS FROM CLASSES AS C
	WHERE NUMGUNS>= ALL(SELECT NUMGUNS FROM CLASSES AS D WHERE C.BORE = D.BORE)

SELECT NAME FROM SHIPS 
	JOIN CLASSES AS C ON SHIPS.CLASS = C.CLASS
	WHERE C.NUMGUNS>=ALL(SELECT NUMGUNS FROM CLASSES AS C1 WHERE C.BORE=C1.BORE)


