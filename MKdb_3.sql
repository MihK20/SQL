USE movies

SELECT NAME FROM MOVIESTAR AS STAR
	WHERE NAME IN (SELECT NAME FROM MOVIEEXEC WHERE NETWORTH>10000000) AND GENDER='F'

SELECT NAME FROM MOVIESTAR
	WHERE NAME NOT IN (SELECT NAME FROM MOVIEEXEC)

USE pc

SELECT DISTINCT MAKER FROM PRODUCT
	WHERE MODEL IN (SELECT MODEL FROM PC WHERE SPEED>=500)

SELECT * FROM LAPTOP 
	WHERE speed<ALL(SELECT SPEED FROM PC)

SELECT P.MODEL FROM PRODUCT AS P
	JOIN (SELECT MODEL, PRICE FROM PC
			UNION
		SELECT MODEL, PRICE FROM LAPTOP
			UNION
		SELECT MODEL, PRICE FROM PRINTER) AS M
			ON M.MODEL = P.MODEL
			WHERE M.PRICE>=ALL((SELECT PRICE FROM PC
								UNION
							SELECT PRICE FROM LAPTOP
								UNION
							SELECT PRICE FROM PRINTER))

SELECT MAKER FROM PRODUCT
	JOIN PRINTER ON PRODUCT.MODEL= PRINTER.MODEL
	WHERE PRICE<=ALL(SELECT PRICE FROM PRINTER WHERE color='y') and PRODUCT.MODEL IN (SELECT MODEL FROM PRINTER WHERE color='y')

SELECT MAKER FROM PRODUCT 
	JOIN (SELECT MODEL,SPEED, RAM FROM PC 
		WHERE RAM <= ALL(SELECT RAM FROM PC) AND SPEED>=ALL(SELECT SPEED FROM PC WHERE RAM<=ALL(SELECT RAM FROM PC))) AS P ON P.MODEL=PRODUCT.MODEL
		
USE SHIPS

