USE movies
SELECT DISTINCT NAME, STARNAME FROM STUDIO
	JOIN MOVIE ON NAME=STUDIONAME
	LEFT JOIN STARSIN ON TITLE = MOVIETITLE

SELECT DISTINCT NAME FROM MOVIEEXEC 
    JOIN MOVIE ON CERT#=PRODUCERC#
	JOIN STARSIN ON TITLE=MOVIETITLE AND STARNAME='Harrison Ford'

SELECT NAME FROM MOVIESTAR
	JOIN STARSIN ON NAME=STARNAME
	JOIN MOVIE ON MOVIETITLE=TITLE AND STUDIONAME='MGM'
	WHERE GENDER='f'
