SELECT TITLE, YEAR, P.NAME FROM MOVIE
	LEFT JOIN MOVIEEXEC AS P ON PRODUCERC#=CERT#

SELECT TITLE, YEAR, P.NAME AS PRODUCER_NAME, S.NAME AS STUDIO_NAME, S.ADDRESS AS STUDIO_ADDRESS FROM MOVIE
	LEFT JOIN MOVIEEXEC AS P ON PRODUCERC#=CERT#
	LEFT JOIN STUDIO AS S ON STUDIONAME=S.NAME

SELECT NAME FROM MOVIESTAR
	WHERE NAME NOT IN(SELECT STARNAME FROM STARSIN)

SELECT NAME FROM STARSIN
	RIGHT JOIN MOVIESTAR ON NAME=STARNAME 
	WHERE MOVIETITLE IS NULL
	

